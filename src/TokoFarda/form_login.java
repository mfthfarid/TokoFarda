    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed

        String id;
        String namaUser;
        String role;
        
        String sql = "select * from user where Username=? AND Password=?";
        
        try{
            pst = conn.prepareStatement(sql);
            pst.setString(1, txt_username.getText());
            pst.setString(2, txt_password.getText());
            
            rs = pst.executeQuery();
            if (rs.next()){
                id = rs.getString("Id_user");
                namaUser = rs.getString("Nama_User");
                role = rs.getString("Level");
                
                session.setU_id(id);
                session.setU_Username(namaUser);
                session.setU_email(role);
                if(rs.getString("Level").equals("Admin")){
                    dash_admin adm = new dash_admin();
                    adm.setVisible(true);
                    adm.setExtendedState(JFrame.MAXIMIZED_BOTH);
                    this.setVisible(false);
                }
                if(rs.getString("Level").equals("Kasir")) {
                    dash_kasir ksr = new dash_kasir();
                    ksr.setVisible(true);
                    ksr.setExtendedState(JFrame.MAXIMIZED_BOTH);
                    this.setVisible(false);
                }
                if(rs.getString("Level").equals("Owner")) {
                    dash_owner own = new dash_owner();
                    own.setVisible(true);
                    own.setExtendedState(JFrame.MAXIMIZED_BOTH);
                    this.setVisible(false);
                }
                
            } else {
                JOptionPane.showMessageDialog(null, "Username and Password is INCORRECT");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btn_loginActionPerformed

