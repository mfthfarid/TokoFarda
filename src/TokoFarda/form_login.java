    private void lupaPasswordMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lupaPasswordMouseClicked
        // TODO add your handling code here:
        String email = JOptionPane.showInputDialog(null, "Masukkan alamat email", "Lupa Password", JOptionPane.PLAIN_MESSAGE);
        
        String sql = "select * from user where Email=?";
        
        try{
            pst = conn.prepareStatement(sql);
            pst.setString(1, email);
            rs = pst.executeQuery();
            if(rs.next()){
                session.setEmail(email);
                JTextField jawabanField = new JTextField();
                Object[] message = {
                    "Pertanyaan keamanan:", 
                    "Berapa nomor toko?",
                    "Jawaban:", jawabanField
                };
                int option = JOptionPane.showConfirmDialog(null, message, "Lupa Password", JOptionPane.OK_CANCEL_OPTION);

                if (option == JOptionPane.OK_OPTION) {
                    String jawabanKeamanan = jawabanField.getText();

                    if (jawabanKeamanan.equals("192.168.58.1")) {
                        JOptionPane.showMessageDialog(null, "Jawaban keamanan cocok, Anda dapat mengatur password baru");
                        resetCode rc = new resetCode();
                        rc.setVisible(true);
                        rc.setLocationRelativeTo(null);
                        rc.setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);
                    } else {
                        JOptionPane.showMessageDialog(null, "Jawaban keamanan tidak cocok");
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Alamat email tidak ditemnukan");
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_lupaPasswordMouseClicked

